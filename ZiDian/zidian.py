# 字典的key必须是不可变的类型，列表不能作为字典的key，元组、字符串都可以
# 字典的key必须唯一，不可重复
# 字典跟列表一样，也是可变的
# 字典中的value可以是任意类型
# 字典内键值对没有顺序，适合存储大规模的数据，检索通过key进行，速度快

# 字典构建方法1
d = {}
d[1] = "liao"
d[2] = "mi"
d[3] = "xi"
print(d)

# 字典构建方法2
d = {}
t = ([1, "liao"], [2, "mi"], [3, "xi"])
d = dict(t)
print(d)

# 字典构建方法3
d = {}
d = d.fromkeys((1, 2, 3), "liao")
print(d) # 输出 {1: 'liao', 2: 'liao', 3: 'liao'}






